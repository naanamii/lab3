def number_to_words(number):
    ones = ['', 'один', 'два', 'три', 'четыре', 'пять', 'шесть', 'семь', 'восемь', 'девять']
    teens = ['десять', 'одиннадцать', 'двенадцать', 'тринадцать', 'четырнадцать', 'пятнадцать', 'шестнадцать', 'семнадцать', 'восемнадцать', 'девятнадцать']
    tens = ['', '', 'двадцать', 'тридцать', 'сорок', 'пятьдесят', 'шестьдесят', 'семьдесят', 'восемьдесят', 'девяносто']
    hundreds = ['', 'сто', 'двести', 'триста', 'четыреста', 'пятьсот', 'шестьсот', 'семьсот', 'восемьсот', 'девятьсот']
    if number == 0:
        return 'ноль'
    result = ''
    if number // 100:
        result += hundreds[number // 100] + ' '
    if number % 100 // 10 == 1:
        result += teens[number % 10] 
    else:
        result += tens[number % 100 // 10] + ' '
        result += ones[number % 10]
    return result
try:
    number = int(input("Введите число от 1 до 1000: "))
    if 1 <= number <= 1000:
        number_in_words = number_to_words(number)
        print(f"Текстовое представление числа {number}: {number_in_words}")
    else:
        print("Число должно быть в диапазоне от 1 до 1000.")
except ValueError:
    print("Пожалуйста, введите целое число.")
